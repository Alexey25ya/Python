def dif_max_min_fract(l1):
    '''Разность между максимальным и минимальным значением дробной части элементов'''
    l2=[]
    for i  in range(len(l1)):
        l2.insert(i,l1[i]%1)
    print(f'Разность между максимальным и минимальным значением дробной части элементов: ',round((max(l2)-min(l2)),2)) 
