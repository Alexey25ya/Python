def multi_of_pairs(list_nums):
    '''произведение пар чисел списка''' 
    l=[]
    if  len(list_nums)%2==1:
        length=len(list_nums)//2+1
    else: length=len(list_nums)//2
    for i  in range(length):
            mult=list_nums[i]*list_nums[len(list_nums)-i-1]
            l.insert(i,mult)
    print(f'Произведение пар чисел заданного массива: ',l)
